def fahrenheit_to_celcius_conversion():
    fahrenheit = int(input("Enter degrees in fahrenheit: "))
    fahrenheit_to_celcius = (fahrenheit - 32) * 5 / 9
    print("The degrees in celcius is", fahrenheit_to_celcius)
fahrenheit_to_celcius_conversion()
fahrenheit_to_celcius_conversion()